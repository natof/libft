int	ft_atoi(char const *str)
{
	int j;
	int	n;

	j = 1;
	n = 0;
	while (*str == ' ' || (*str >= '\t' && *str <= '\r'))
		str++;
	if ((*str == '-' || *str == '+') && *str++ == '-')
        j = -1;
	while (*str >= '0' && *str <= '9' && *str != '\0')
		n = n * 10 + (*str++ - '0');
	n *= j;
	if(j == -1 && n > 0)
		return (0);
	if (j == 1 && n < 0)
		return (-1);
	return (n);
}
