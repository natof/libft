int	ft_atoi(char *str)
{
	int	j;
	int	n;

	j = 1;
	n = 0;
	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	if (*str == '-')
	{
		j *= -1;
		str++;
	}
	while (*str != '\0')
	{
		if ((*str >= '0' && *str <= '9'))
			n = n * 10 + *str - '0';
		else
			break ;
		str++;
	}
	return (j *= n);
}
